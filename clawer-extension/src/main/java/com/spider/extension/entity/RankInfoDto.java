package com.spider.extension.entity;

public class RankInfoDto {

    private String id;

    private String type;

    private String videoName;

    private String contentType;

    private String videoRanking;

    private String videoRate;

    private String videoReleaseDates;

    private String videoCast;

    private String videoBoxOffice;

    private String updateDate;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getVideoName() {
        return videoName;
    }

    public void setVideoName(String videoName) {
        this.videoName = videoName;
    }

    public String getContentType() {
        return contentType;
    }

    public void setContentType(String contentType) {
        this.contentType = contentType;
    }

    public String getVideoRanking() {
        return videoRanking;
    }

    public void setVideoRanking(String videoRanking) {
        this.videoRanking = videoRanking;
    }

    public String getVideoRate() {
        return videoRate;
    }

    public void setVideoRate(String videoRate) {
        this.videoRate = videoRate;
    }

    public String getVideoReleaseDates() {
        return videoReleaseDates;
    }

    public void setVideoReleaseDates(String videoReleaseDates) {
        this.videoReleaseDates = videoReleaseDates;
    }

    public String getVideoCast() {
        return videoCast;
    }

    public void setVideoCast(String videoCast) {
        this.videoCast = videoCast;
    }

    public String getVideoBoxOffice() {
        return videoBoxOffice;
    }

    public void setVideoBoxOffice(String videoBoxOffice) {
        this.videoBoxOffice = videoBoxOffice;
    }

    public String getUpdateDate() {
        return updateDate;
    }

    public void setUpdateDate(String updateDate) {
        this.updateDate = updateDate;
    }
}
